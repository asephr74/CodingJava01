    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Float eff,cycletime,hourea;
        
        cycletime=(((Float.parseFloat(pointTextField.getText())*Float.parseFloat(arrayTextField.getText()))*3600)/(Float.parseFloat(targetTextField.getText())));
        hourea=(Float.parseFloat(arrayTextField.getText())*(3600/cycletime));
        
        eff=(hourea*85)/100;
        
        cycleTextField.setText(String.format("%.0f",cycletime));
        houreaTextField.setText(String.format("%.0f",hourea));
        effTextField.setText(String.format("%.0f", eff));
       
    }//GEN-LAST:event_jButton1ActionPerformed

